function teams(string, number) {
  const array = string.split(", ");
  const numberOfTeams = array.length / number;
  const arrOfTeams = [];
  for (let i = 0; i < numberOfTeams; i++) {
    arrOfTeams.push(array.slice(number * i, number * (i + 1)));
  }
  const result = [];
  for (let i = 0; i < number; i++) {
    const teamMembers = [];
    for (let j = 0; j < arrOfTeams.length; j++) {
      if (arrOfTeams[j][i]) {
        teamMembers.push(arrOfTeams[j][i]);
      }
    }
    result.push(`Team ${i + 1}: ${teamMembers.join(", ")}`);
  }
  return result;
}

